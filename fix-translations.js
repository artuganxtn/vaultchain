// Check for duplicates in all files
function checkDuplicates(file, name) {
  const data = JSON.parse(fs.readFileSync(file, 'utf8'));
  const keys = Object.keys(data);
  const values = Object.values(data);
  const duplicateKeys = keys.filter((key, index) => keys.indexOf(key) !== index);
  const duplicateValues = values.filter((val, index) => values.indexOf(val) !== index && val);
  
  if (duplicateKeys.length > 0) {
    console.log(`⚠️  ${name}: Found ${duplicateKeys.length} duplicate keys`);
  }
  if (duplicateValues.length > 0) {
    console.log(`⚠️  ${name}: Found ${duplicateValues.length} duplicate values (may be intentional)`);
  }
  return { duplicateKeys, duplicateValues };
}

